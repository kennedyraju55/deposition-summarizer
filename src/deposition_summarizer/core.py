def get_significance_color(significance: str) -> str:
    """Return a Rich color string for the given significance level."""
    colors = {
        "low": "green",
        "medium": "yellow",
        "high": "red",
        "critical": "bold red",
    }
    return colors.get(significance.lower(), "white")


