def summarize_deposition(transcript: str, model: str = "gemma4:latest") -> DepositionSummary:
    """Generate a comprehensive summary of a deposition transcript.

    Args:
        transcript: The full deposition transcript text.
        model: LLM model name to use.

    Returns:
        A DepositionSummary with all analysis fields populated.
    """
    logger.info("Summarizing deposition: %d chars", len(transcript))
    cfg = load_config()
    max_len = cfg.get("analysis", {}).get("max_transcript_length", 50000)
    if len(transcript) > max_len:
        transcript = transcript[:max_len] + "\n\n[... transcript truncated ...]"

    prompt = f"""Analyze the following deposition transcript and provide a comprehensive summary.

Return your analysis as JSON with this exact structure:
{{
    "title": "Brief title for this deposition",
    "witness": "Name of the deponent/witness",
    "date": "Date of deposition if mentioned",
    "duration": "Duration if mentioned",
    "overall_summary": "2-3 paragraph comprehensive summary",
    "key_admissions": [
        {{"speaker": "name", "statement": "the admission", "page_line": "p.X, ln.Y", "significance": "high"}}
    ],
    "contradictions": [
        {{"statement_a": "first statement", "statement_b": "contradicting statement", "page_line_a": "p.X", "page_line_b": "p.Y", "explanation": "why these contradict"}}
    ],
    "important_testimony": [
        {{"speaker": "name", "topic": "topic", "summary": "summary of testimony", "page_line": "p.X", "relevance": "high"}}
    ],
    "witness_profile": {{
        "name": "witness name",
        "role": "their role",
        "credibility_notes": ["note1", "note2"],
        "demeanor_notes": ["note1"]
    }},
    "topics_covered": ["topic1", "topic2"],
    "follow_up_questions": ["question1", "question2"],
    "timeline": [
        {{"date": "date", "event": "what happened"}}
    ]
}}

DEPOSITION TRANSCRIPT:
{transcript}"""

    messages = [{"role": "user", "content": prompt}]
    response = chat(
        messages=messages,
        model=model,
        system_prompt=SYSTEM_PROMPT,
        temperature=cfg["llm"]["temperature"],
        max_tokens=cfg["llm"]["max_tokens"],
    )

    try:
        data = _parse_json_response(response)
    except ValueError:
        logger.warning("Failed to parse structured response, returning raw summary")
        return DepositionSummary(overall_summary=response)

    summary = DepositionSummary(
        title=data.get("title", ""),
        witness=data.get("witness", ""),
        date=data.get("date", ""),
        duration=data.get("duration", ""),
        overall_summary=data.get("overall_summary", ""),
        topics_covered=data.get("topics_covered", []),
        follow_up_questions=data.get("follow_up_questions", []),
    )

    for adm in data.get("key_admissions", []):
        summary.key_admissions.append(KeyAdmission(
            speaker=adm.get("speaker", ""),
            statement=adm.get("statement", ""),
            page_line=adm.get("page_line", ""),
            significance=adm.get("significance", "medium"),
        ))

    for con in data.get("contradictions", []):
        summary.contradictions.append(Contradiction(
            statement_a=con.get("statement_a", ""),
            statement_b=con.get("statement_b", ""),
            page_line_a=con.get("page_line_a", ""),
            page_line_b=con.get("page_line_b", ""),
            explanation=con.get("explanation", ""),
        ))

    for test in data.get("important_testimony", []):
        summary.important_testimony.append(ImportantTestimony(
            speaker=test.get("speaker", ""),
            topic=test.get("topic", ""),
            summary=test.get("summary", ""),
            page_line=test.get("page_line", ""),
            relevance=test.get("relevance", "medium"),
        ))

    wp = data.get("witness_profile", {})
    summary.witness_profile = WitnessProfile(
        name=wp.get("name", ""),
        role=wp.get("role", ""),
        credibility_notes=wp.get("credibility_notes", []),
        demeanor_notes=wp.get("demeanor_notes", []),
    )

    for tl in data.get("timeline", []):
        summary.timeline.append({"date": tl.get("date", ""), "event": tl.get("event", "")})

    return summary


