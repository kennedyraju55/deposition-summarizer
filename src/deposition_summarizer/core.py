@dataclass
class KeyAdmission:
    """A key admission made during deposition testimony."""
    speaker: str
    statement: str
    page_line: str = ""
    significance: str = "medium"  # low, medium, high, critical


@dataclass
class Contradiction:
    """A contradiction detected between two statements in the deposition."""
    statement_a: str
    statement_b: str
    page_line_a: str = ""
    page_line_b: str = ""
    explanation: str = ""


@dataclass
class ImportantTestimony:
    """A piece of important testimony extracted from the deposition."""
    speaker: str
    topic: str
    summary: str
    page_line: str = ""
    relevance: str = "medium"  # low, medium, high, critical


@dataclass
class WitnessProfile:
    """Profile of a deposition witness."""
    name: str = ""
    role: str = ""
    credibility_notes: List[str] = field(default_factory=list)
    demeanor_notes: List[str] = field(default_factory=list)


@dataclass
class DepositionSummary:
    """Complete summary of a deposition transcript."""
    title: str = ""
    witness: str = ""
    date: str = ""
    duration: str = ""
    overall_summary: str = ""
    key_admissions: List[KeyAdmission] = field(default_factory=list)
    contradictions: List[Contradiction] = field(default_factory=list)
    important_testimony: List[ImportantTestimony] = field(default_factory=list)
    witness_profile: WitnessProfile = field(default_factory=WitnessProfile)
    topics_covered: List[str] = field(default_factory=list)
    follow_up_questions: List[str] = field(default_factory=list)
    timeline: List[Dict[str, str]] = field(default_factory=list)


